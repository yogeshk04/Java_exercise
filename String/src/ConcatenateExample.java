
public class ConcatenateExample {
	public static void main(String[] args) {
		String s = "Welcome";
		//s.concat("to you!");
		System.out.println(s);
		System.out.println();
		
		s = s.concat(" Siemens").concat(" PLM").concat(" Software").concat(" Pune");
		System.out.println(s);

		System.out.println(Math.abs(10.5));
		System.out.println(Math.abs(-10.5));
	}

}
